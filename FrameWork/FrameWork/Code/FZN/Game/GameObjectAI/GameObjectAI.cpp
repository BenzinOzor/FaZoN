#include "FZN/Includes.h"
#include "FZN/Managers/AIManager.h"
#include "FZN/Game/GameObjectAI/GameObjectAI.h"


namespace fzn
{
	/////////////////CONSTRUCTOR / DESTRUCTOR/////////////////

	//------------------------------------------------------------------------------------------------------------------------------------------------------------------
	//Default constructor
	//------------------------------------------------------------------------------------------------------------------------------------------------------------------
	GameObjectAI::GameObjectAI()
	{
		m_owner = this;
		m_ID = -1;

		if(g_pFZN_Core->IsAIModuleActivated())
		{
			m_ID = NewID;
			g_pFZN_AIMgr->Store(this);
		}
	}

	//------------------------------------------------------------------------------------------------------------------------------------------------------------------
	//Default destructor
	//------------------------------------------------------------------------------------------------------------------------------------------------------------------
	GameObjectAI::~GameObjectAI()
	{
		if(g_pFZN_Core->IsAIModuleActivated())
			g_pFZN_AIMgr->Remove(m_ID);
	}


	/////////////////ACCESSORS / MUTATORS/////////////////

	//------------------------------------------------------------------------------------------------------------------------------------------------------------------
	//Accessor on the ID of the GameObject
	//Return value : ID
	//------------------------------------------------------------------------------------------------------------------------------------------------------------------
	int GameObjectAI::GetID()
	{
		return m_ID;
	}
} //namespace fzn
